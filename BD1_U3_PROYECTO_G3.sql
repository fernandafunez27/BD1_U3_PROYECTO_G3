SELECT EN.IdEnvio, CLI.Nombre, CLI.Apellido, EN.Monto, EN.Fecha
FROM ENVIO EN
INNER JOIN PRODUCTOS_CARRITO PC ON PC.IdEnvio=EN.IdEnvio
