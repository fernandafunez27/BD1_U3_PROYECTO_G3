INNER JOIN CLIENTE CLI ON CLI.IdCliente=CA.IdCliente
WHERE  EN.Fecha BETWEEN '01-01-2022' AND '31-07-2022'
GROUP BY EN.IdEnvio, CLI.Nombre, CLI.Apellido, EN.Monto, EN.Fecha
ORDER BY EN.Fecha;


